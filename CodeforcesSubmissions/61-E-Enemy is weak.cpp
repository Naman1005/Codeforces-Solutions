class Fenwick {
private:
    vll fen;
    ll n;
 public:
    Fenwick(ll size) : n(size) { fen.assign(n + 1, 0); }
    // increments ele at i by val
    void update(ll i, ll val) {
        while (i <= n) fen[i] += val, i += (i & -i);
    }
    // overwrites ele at i by val
    void overwrite(ll i, ll val) {
        ll curr = query(i) - query(i - 1);
        ll diff = val - curr;
        update(i, diff);
    }
    // sum of 1 to i
    ll query(ll i) {
        ll sum = 0;
        while (i > 0) sum += fen[i], i -= (i & -i);
        return sum;
    }
    // sum of l to r (both inclusive)
    ll get(ll l, ll r) { return query(r) - query(l - 1); }
};
